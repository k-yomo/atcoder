fn main() {
    input! {
        n: usize,
        s: Chars,
    }

    let m = (10_i128.pow(9) + 7);
    let mut dp = vec![0_i128; 7];
    for c in s {
        match c {
            'a' => dp[0] += 1,
            't' => dp[1] += dp[0] % m,
            'c' => dp[2] += dp[1] % m,
            'o' => dp[3] += dp[2] % m,
            'd' => dp[4] += dp[3] % m,
            'e' => dp[5] += dp[4] % m,
            'r' => dp[6] += dp[5] % m,
            _ => {}
        }
    }

    println!("{}", dp[6] % m)
}
