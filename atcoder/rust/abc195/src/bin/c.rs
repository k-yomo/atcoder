fn main() {
    input! {
        mut n: usize,
    }

    if n < 999 {
        return println!("0");
    }

    let counts = vec![999999999999999, 999999999999, 999999999, 999999, 999];
    let mut ans = 0;
    for (i, c) in counts.iter().enumerate() {
        if n > *c {
            ans += (n - c) * (5 - i);
            n -= (n - c);
        }
    }

    println!("{:?}", ans);
}
