fn main() {
    input! {
        n: usize,
        c: [usize; n],
        ab: [(usize, usize); n-1],
    }

    let mut deps = petgraph::Graph::<&str, f64>::new();
    for i in c {
        let a = deps.add_node(0, i);
    }
    let b = deps.add_node("hello");
    deps.update_edge(a, b, 5.0);
    deps.update_edge(b, a, 10.0);

    let result = bellman_ford(&deps, NodeIndex::new(0));
    println!("{:?}", result);
}
